#include "Init.h"




void per_init(void)
{
    /* ---------- 段选全灭 Y5 ---------- */
    P0 = 0xFF;
    P2 = P2 & 0x1F | 0xA0;   // 选 Y5
    P2 = P2 & 0x1F;          // 锁存

    /* ---------- 位选全灭 Y6 ---------- */
    P0 = 0xFF;
    P2 = P2 & 0x1F | 0xC0;   // 选 Y6
    P2 = P2 & 0x1F;          // 锁存

    /* ---------- LED 初始全灭 ---------- */
    P0 = 0xFF;
    P2 = P2 & 0x1F | 0x80;   // 选 Y4
    P2 = P2 & 0x1F;
}



