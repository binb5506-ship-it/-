#include "key.h"

/* =========================================================
 * 矩阵按键扫描
 *   扫描顺序：行线依次拉低（P44→P42→P35），读列线（P30~P33）
 *   返回值：按下的按键编号，无按键返回 0
 *
 *   特殊处理：
 *   1. P30 同时复用为 UART RXD，串口接收时该引脚为低，
 *      每次换行前先检查 P30，若为低则说明串口正忙，直接返回 0
 *   2. S8(P33)+S9(P32) 同时按下时，优先返回 89
 * ========================================================= */
unsigned char Key_Read()
{
    unsigned char temp = 0;

    /* ---- 行 1（P44 拉低）扫描前：先确认串口不忙 ---- */
    P44=1; P42=1; P35=1;
    if(P30 == 0) return 0;  /* P30 为低 = 串口在接收，退出避免误判 */

    P44=0; P42=1; P35=1;    /* 拉低第一行 */
    if(P30 == 0) temp = 7;
    if(P31 == 0) temp = 6;
    if(P32 == 0) temp = 5;
    if(P33 == 0) temp = 4;

    /* ---- 行 2（P42 拉低）扫描前：再次检查串口 ---- */
    P44=1; P42=1; P35=1;
    if(P30 == 0) return 0;  /* 原来缺少此检查，换行前必须重置并检测 */

    P44=1; P42=0; P35=1;    /* 拉低第二行 */
    if(P30 == 0) temp = 11;
    if(P31 == 0) temp = 10;

    /* S8+S9 同时按下：优先判断，用 return 立即退出，避免被后面的单键覆盖 */
    if((P32 == 0) && (P33 == 0)) return 89; /* 原来放在后面且用 temp=89 会被单键覆盖 */

    if(P32 == 0) temp = 9;
    if(P33 == 0) temp = 8;

    /* ---- 行 3（P35 拉低）扫描前：再次检查串口 ---- */
    P44=1; P42=1; P35=1;
    if(P30 == 0) return 0;  /* 原来缺少此检查 */

    P44=1; P42=1; P35=0;    /* 拉低第三行 */
    if(P30 == 0) temp = 15;
    if(P31 == 0) temp = 14;
    if(P32 == 0) temp = 13;
    if(P33 == 0) temp = 12;

    /* ---- 恢复所有行线为高，释放总线 ---- */
    P44=1; P42=1; P35=1;
    P3 = P3 | 0xef; /* 恢复 P3 口（保留 P3.4 原状，其余置1）原来缺少此恢复 */

    return temp;
}
