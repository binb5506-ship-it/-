#include "init.h"

/* =========================================================
 * 系统硬件初始化
 *   上电时将所有外设置于已知的安全初始状态：
 *   LED 全灭、继电器/蜂鸣器/电机全关。
 *
 *   CT107D 开发板通过 74HC138 译码器用 P2[7:5] 选通锁存器：
 *     P2[7:5] = 100 (0x80) → Y4 → LED 锁存器
 *     P2[7:5] = 101 (0xa0) → Y5 → 继电器/蜂鸣器/电机锁存器
 *   写入 P0 后拉高再拉低 P2[7:5] 完成一次锁存。
 * ========================================================= */
void System_Init(void)
{
    unsigned char temp;

    /* ---- 第一步：关闭所有 LED ---- */
    /* LED 低电平点亮，P0=0xff 使所有 LED 熄灭 */
    P0   = 0xff;
    temp = P2 & 0x1f | 0x80;   /* P2[7:5]=100，选通 LED 锁存器 */
    P2   = temp;
    temp = P2 & 0x1f;           /* P2[7:5]=000，关闭选通（锁存数据） */
    P2   = temp;

    /* ---- 第二步：关闭继电器 / 蜂鸣器 / 电机 ---- */
    /* 这些外设高电平有效，P0=0x00 使其全部断开 */
    P0   = 0x00;
    temp = P2 & 0x1f | 0xa0;   /* P2[7:5]=101，选通继电器等锁存器 */
    P2   = temp;
    temp = P2 & 0x1f;           /* P2[7:5]=000，关闭选通（锁存数据） */
    P2   = temp;
}
